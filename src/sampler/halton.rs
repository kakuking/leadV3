#[inline]
fn reverse_bits_64(x: u64) -> u64 {
    x.reverse_bits()
}

fn radical_inverse(base_index: usize, a: u64) -> f32 {
    if base_index >= PRIME_TABLE_SIZE {
        panic!("Unsupported base_index {}", base_index);
    }

    if base_index == 0 {
        const SCALE: f64 = 1.0 / ((1u128 << 64) as f64);
        ((reverse_bits_64(a) as f64) * SCALE) as f32
    } else {
        radical_inverse_base(PRIMES[base_index], a)
    }
}

fn radical_inverse_base(base: u64, mut a: u64) -> f32 {
    let inv_base = 1.0 / base as f32;
    let mut reversed_digits = 0u64;
    let mut inv_base_n = 1.0f32;

    while a != 0 {
        let next = a / base;
        let digit = a - next * base;
        reversed_digits = reversed_digits * base + digit;
        inv_base_n *= inv_base;
        a = next;
    }

    ((reversed_digits as f32) * inv_base_n).min(ONE_MINUS_EPSILON)
}

fn inverse_radical_inverse<const BASE: u64>(mut inverse: u64, n_digits: usize) -> u64 {
    let mut index = 0u64;

    for _ in 0..n_digits {
        let digit = inverse % BASE;
        inverse /= BASE;
        index = index * BASE + digit;
    }

    index
}

fn compute_radical_inverse_permutations(rng: &mut RNG) -> Vec<u16> {
    let mut perm_array_size = 0usize;
    for i in 0..PRIME_TABLE_SIZE {
        perm_array_size += PRIMES[i] as usize;
    }

    let mut perms = vec![0u16; perm_array_size];

    let mut offset = 0usize;
    for i in 0..PRIME_TABLE_SIZE {
        let prime = PRIMES[i] as usize;
        let slice = &mut perms[offset..offset + prime];

        for j in 0..prime {
            slice[j] = j as u16;
        }

        shuffle_u16(slice, rng);

        offset += prime;
    }

    perms
}

fn shuffle_u16(data: &mut [u16], rng: &mut RNG) {
    for i in 0..data.len() {
        let other = i + rng.uniform_u32_bounded((data.len() - i) as u32) as usize;
        data.swap(i, other);
    }
}

fn scrambled_radical_inverse(base_index: usize, a: u64, perm: &[u16]) -> f32 {
    if base_index >= PRIME_TABLE_SIZE {
        panic!("Unsupported base_index {}", base_index);
    }

    scrambled_radical_inverse_base(PRIMES[base_index], perm, a)
}

fn scrambled_radical_inverse_base(base: u64, perm: &[u16], mut a: u64) -> f32 {
    debug_assert!(perm.len() >= base as usize);

    let inv_base = 1.0 / base as f32;
    let mut reversed_digits = 0u64;
    let mut inv_base_n = 1.0f32;

    while a != 0 {
        let next = a / base;
        let digit = a - next * base;
        reversed_digits = reversed_digits * base + perm[digit as usize] as u64;
        inv_base_n *= inv_base;
        a = next;
    }

    let correction = inv_base * perm[0] as f32 / (1.0 - inv_base);

    (inv_base_n * (reversed_digits as f32 + correction)).min(ONE_MINUS_EPSILON)
}

// halton sampler here
// which needs globoal sampler
#[derive(Clone)]
pub struct HaltonSampler {
    // sampler base
    base: SamplerBase,
    rng: RNG,

    // global sampler stuff
    dimension: usize,
    interval_sample_index: usize,
    array_end_dim: usize,

    // halton sampler stuff
    radical_inverse_permutations: Vec<u16>,
    base_scales: [usize; 2],
    base_exponents: [usize; 2],
    sample_stride: usize,
    mult_inverse: [usize; 2],
    pixel_for_offset: Point2, // default is pixel(max, max)
    offset_for_current_pixel: usize,
}

impl HaltonSampler {
    pub fn new() -> Self {
        Self {
            base: SamplerBase::new(),
            rng: RNG::init(0),
            dimension: 0,
            interval_sample_index: 0,
            array_end_dim: 0,
            radical_inverse_permutations: Vec::new(),
            base_scales: [0, 0],
            base_exponents: [0, 0],
            sample_stride: 0,
            mult_inverse: [0, 0],
            pixel_for_offset: Point2::new(MAX, MAX),
            offset_for_current_pixel: 0,
        }
    }

    fn permutation_for_dimension(&self, dim: usize) -> &[u16] {
        if dim >= PRIME_TABLE_SIZE {
            panic!("Halton sampler can only sample {} dimensions", PRIME_TABLE_SIZE);
        }

        let start = PRIME_SUMS[dim] as usize;
        let len = PRIMES[dim] as usize;

        &self.radical_inverse_permutations[start..start+len]
    }

    pub fn init(samples_per_pixel: usize, sample_bounds: Bounds2) -> Self {
        let mut ret = Self::new();
        ret.base.samples_per_pixel = samples_per_pixel;

        if ret.radical_inverse_permutations.len() == 0 {
            ret.radical_inverse_permutations = compute_radical_inverse_permutations(&mut ret.rng)
        }

        let res = sample_bounds.p_max - sample_bounds.p_min;

        for i in 0..2 {
            let base = if i == 0 { 2.0 } else { 3.0 };
            let mut scale = 1.0;
            let mut exp = 0.0;

            loop {
                if scale >= res[i].min(K_MAX_RESOLUTION as f32) {
                    break;
                }

                scale *= base;
                exp += 1.0;
            }

            ret.base_scales[i] = scale as usize;
            ret.base_exponents[i] = exp as usize;
        }

        ret.sample_stride = (ret.base_scales[0] * ret.base_scales[1]) as usize;

        ret.mult_inverse[0] = multiplicative_inverse(ret.base_scales[1] as usize, ret.base_scales[0] as usize);
        ret.mult_inverse[1] = multiplicative_inverse(ret.base_scales[0] as usize, ret.base_scales[1] as usize);

        ret
    }
}

impl GlobalSamplerT for HaltonSampler {
    fn get_samples_per_pixel(&self) -> usize { self.base.samples_per_pixel }
    fn get_array_1d_offset(&self) -> usize { self.base.array_1d_offset }
    fn get_array_2d_offset(&self) -> usize { self.base.array_2d_offset }
    fn get_current_pixel_sampler_index(&self) -> usize { self.base.current_pixel_sampler_index }
    fn get_current_pixel(&self) -> Point2 { self.base.current_pixel.clone() }
    fn get_dimension(&self) -> usize { self.dimension }
    fn get_interval_sample_index(&self) -> usize { self.interval_sample_index }
    fn get_array_end_dim(&self) -> usize { self.array_end_dim }

    fn set_samples_per_pixel(&mut self, new_value: usize) { self.base.samples_per_pixel = new_value; }
    fn set_array_1d_offset(&mut self, new_value: usize) { self.base.array_1d_offset = new_value; }
    fn set_array_2d_offset(&mut self, new_value: usize) { self.base.array_2d_offset = new_value; }
    fn set_current_pixel(&mut self, new_value: Point2) { self.base.current_pixel = new_value; }
    fn set_current_pixel_sampler_index(&mut self, new_value: usize) { self.base.current_pixel_sampler_index = new_value; }
    fn set_dimension(&mut self, new_value: usize) { self.dimension = new_value; }
    fn set_interval_sample_index(&mut self, new_value: usize) { self.interval_sample_index = new_value; }
    fn set_array_end_dim(&mut self, new_value: usize) { self.array_end_dim = new_value; }

    fn get_samples_1d_array_sizes(&mut self) -> &mut Vec<usize> { &mut self.base.samples_1d_array_sizes }
    fn get_samples_2d_array_sizes(&mut self) -> &mut Vec<usize> { &mut self.base.samples_2d_array_sizes }
    fn get_samples_1d_array(&mut self) -> &mut Vec<Vec<f32>> { &mut self.base.samples_1d_array }
    fn get_samples_2d_array(&mut self) -> &mut Vec<Vec<Point2>> { &mut self.base.samples_2d_array }

    fn get_index_for_sample(&mut self, sample_num: usize) -> usize {
        if self.base.current_pixel != self.pixel_for_offset {
            self.offset_for_current_pixel = 0;

            if self.sample_stride > 1 {
                let pm = [
                    mod_i32(self.base.current_pixel[0] as i32, K_MAX_RESOLUTION as i32) as u64,
                    mod_i32(self.base.current_pixel[1] as i32, K_MAX_RESOLUTION as i32) as u64,
                ];

                for i in 0..2 {
                    let dim_offset = if i == 0 {
                        inverse_radical_inverse::<2>(pm[i], self.base_exponents[i] as usize)
                    } else {
                        inverse_radical_inverse::<3>(pm[i], self.base_exponents[i] as usize)
                    };

                    let term =
                        dim_offset
                        * (self.sample_stride / self.base_scales[i]) as u64
                        * self.mult_inverse[i] as u64;

                    let cur_offset = self.offset_for_current_pixel;
                    self.offset_for_current_pixel = cur_offset + term as usize;
                }

                let cur = self.offset_for_current_pixel;
                self.offset_for_current_pixel = cur % self.sample_stride;
            }

            self.pixel_for_offset = self.get_current_pixel().clone();
        }

        self.offset_for_current_pixel + sample_num * self.sample_stride
    }

    fn sample_dimension(&self, idx: usize, dim: usize) -> f32 {
        if dim == 0 {
            radical_inverse(dim, (idx >> self.base_exponents[0]) as u64)
        } else if dim == 1 {
            radical_inverse(dim, (idx / self.base_scales[1]) as u64)
        } else {
            scrambled_radical_inverse(dim, idx as u64, self.permutation_for_dimension(dim))
        }
    }

    fn clone_with_seed(&self, seed: usize) -> Self {
        Self {
            base: SamplerBase {
                samples_per_pixel: self.base.samples_per_pixel,
                current_pixel: Point2::origin(),
                current_pixel_sampler_index: 0,
                samples_1d_array_sizes: self.base.samples_1d_array_sizes.clone(),
                samples_2d_array_sizes: self.base.samples_2d_array_sizes.clone(),
                samples_1d_array: self.base.samples_1d_array.clone(),
                samples_2d_array: self.base.samples_2d_array.clone(),
                array_1d_offset: 0,
                array_2d_offset: 0,
            },
            rng: RNG::init(seed as u64),
            dimension: 0,
            interval_sample_index: 0,
            array_end_dim: self.array_end_dim,
            radical_inverse_permutations: self.radical_inverse_permutations.clone(),
            base_scales: self.base_scales,
            base_exponents: self.base_exponents,
            sample_stride: self.sample_stride,
            mult_inverse: self.mult_inverse,
            pixel_for_offset: Point2::new(MAX, MAX),
            offset_for_current_pixel: 0,
        }
    }
}

impl Manufacturable<Sampler> for HaltonSampler {
    fn create_from_parameters(param: Parameters) -> Sampler {
        let spp = param.get_int("samples_per_pixel", Some(8));
        let sample_bounds_pmin = param.get_point2("sample_bound_min", Some(Point2::origin()));
        let sample_bounds_pmax = param.get_point2("sample_bound_max", Some(Point2::new(1.0, 1.0)));

        let sample_bounds = Bounds2::init_two(&sample_bounds_pmin, &sample_bounds_pmax);

        Sampler::Halton(
            HaltonSampler::init(
                spp as usize, 
                sample_bounds
            )
        )
    }
}

